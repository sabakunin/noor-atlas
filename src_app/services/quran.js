// Tanzil prepends bismillah to the first ayah of every sura except 1 (which IS bismillah) and 9.
// Strip it for display so we can render the standalone bismillah header without duplication.
const BISMILLAH_VARIANTS = [
  "بِسْمِ ٱللَّهِ ٱلرَّحْمَٰنِ ٱلرَّحِيمِ",
  "بِسْمِ ٱللَّٰهِ ٱلرَّحْمَٰنِ ٱلرَّحِيمِ",
  "بِسْمِ اللَّهِ الرَّحْمَٰنِ الرَّحِيمِ",
];
const BOM = "﻿";

function stripLeadingBismillah(text) {
  if (!text) return text;
  let s = text.startsWith(BOM) ? text.slice(1) : text;
  for (const variant of BISMILLAH_VARIANTS) {
    if (s.startsWith(variant)) {
      return s.slice(variant.length).trimStart();
    }
  }
  return s;
}

export function getSura(suraNumber) {
  const meta = surahs[suraNumber - 1];
  if (!meta) return null;
  const ar = arabicData[suraNumber - 1] ?? [];
  const ru = russianData[suraNumber - 1] ?? [];
  const tr = translitData[suraNumber - 1] ?? [];
  const stripBismillah = suraNumber !== 1 && suraNumber !== 9;
  const ayahs = ar.map((arabic, i) => ({
    number: i + 1,
    arabic: stripBismillah && i === 0 ? stripLeadingBismillah(arabic) : arabic,
    translation: ru[i] ?? "",
    transliteration: tr[i] ?? "",
  }));
  return { ...meta, ayahs };
}

