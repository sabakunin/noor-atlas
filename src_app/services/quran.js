export function getSurasInJuz(juzNumber) {
  const start = JUZ_BOUNDARIES.find((j) => j.juz === juzNumber);
  const end = JUZ_BOUNDARIES.find((j) => j.juz === juzNumber + 1);
  if (!start) return [];
  const startSura = start.start.sura;
  const endSura = end ? end.start.sura : 114;
  return surahs.filter((s) => s.number >= startSura && s.number <= endSura);
}
